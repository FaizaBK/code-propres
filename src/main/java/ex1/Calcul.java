package ex1;

// La classe Calcul fournit des opérations mathématiques d'addition et de soustraction

public class Calcul {

    //Méthode add pour l'Addition de deux entiers a et b
    //return La somme des deux entiers.

    public int add(int a, int b) {
        return a + b;
    }

    //Méthode add pour la soustraction de deux entiers a et b
    //return La différence entre les deux entiers

    public int subtract(int a, int b) {
        return a - b;
    }
}
